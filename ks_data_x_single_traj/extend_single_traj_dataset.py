#!/usr/bin/env python3
"""
Safely extend the single-trajectory KS split without changing the current train set.

This script creates a new, self-contained dataset root:
- train is copied exactly from the current split
- existing post-train segments are reused in chronological order
- any remaining val/test segments are generated by continuing the same orbit

The original folders are never modified.

Example
-------
./.venv/bin/python ks_data_x_single_traj/extend_single_traj_dataset.py \
    --target_n_val 10 \
    --target_n_test 10
"""

import argparse
import json
import shutil
import sys
from pathlib import Path

import numpy as np
import torch

THIS_DIR = Path(__file__).resolve().parent
PROJECT_ROOT = THIS_DIR.parent
if str(PROJECT_ROOT) not in sys.path:
    sys.path.append(str(PROJECT_ROOT))

from dataloader.dataloader_ks import save_noisy_ks_data
from ks import generate_ks_segment


def parse_args():
    parser = argparse.ArgumentParser(
        description='Safely extend the single-trajectory KS benchmark split.'
    )
    parser.add_argument('--source_root', default=str(THIS_DIR), type=str,
                        help='Root containing the current ks_single_traj_* folders.')
    parser.add_argument('--train_folder', default='ks_single_traj_train', type=str)
    parser.add_argument('--val_folder', default='ks_single_traj_val', type=str)
    parser.add_argument('--test_folder', default='ks_single_traj_test', type=str)

    parser.add_argument('--out_root', default='ks_single_traj_expanded', type=str,
                        help='Output root for the new self-contained split. '
                             'Relative paths are resolved under source_root.')
    parser.add_argument('--out_train_folder', default='ks_single_traj_train', type=str)
    parser.add_argument('--out_val_folder', default='ks_single_traj_val', type=str)
    parser.add_argument('--out_test_folder', default='ks_single_traj_test', type=str)

    parser.add_argument('--target_n_val', default=10, type=int,
                        help='Number of validation segments in the new split.')
    parser.add_argument('--target_n_test', default=10, type=int,
                        help='Number of test segments in the new split.')

    parser.add_argument('--N', default=256, type=int, help='Spatial dimension')
    parser.add_argument('--L', default=50.0, type=float, help='Domain length parameter')
    parser.add_argument('--dt', default=0.1, type=float, help='Solver timestep')
    parser.add_argument('--t_res', default=10, type=int,
                        help='Save every t_res integration steps')
    parser.add_argument('--T_segment', default=2000.0, type=float,
                        help='Simulation time per segment')
    parser.add_argument('--noise_scales', nargs='*', default=['0.3'],
                        help='Noise scales to ensure in the new split, e.g. 0.3 0.1')
    parser.add_argument('--overwrite', action='store_true',
                        help='Overwrite the output split if it already exists.')
    return parser.parse_args()


def resolve_root(root_arg, source_root):
    root = Path(root_arg)
    if not root.is_absolute():
        root = source_root / root
    return root.resolve()


def clean_files(folder):
    return sorted(
        p for p in folder.glob('0*.pth')
        if '_noise_' not in p.name
    )


def load_clean_sample(path):
    sample = torch.load(path, map_location='cpu', weights_only=False)
    return sample['0'].float(), sample['1'].float()


def copy_sample_bundle(src_clean, dst_clean):
    dst_clean.parent.mkdir(parents=True, exist_ok=True)
    shutil.copy2(src_clean, dst_clean)
    stem = src_clean.stem
    for noise_path in sorted(src_clean.parent.glob(f'{stem}_noise_*.pth')):
        shutil.copy2(noise_path, dst_clean.parent / noise_path.name.replace(stem, dst_clean.stem, 1))


def write_clean_sample(dst_clean, ic, traj):
    torch.save(
        {
            '0': torch.as_tensor(ic, dtype=torch.float32),
            '1': torch.as_tensor(traj, dtype=torch.float32),
        },
        dst_clean,
    )


def write_training_params(folder, clean_paths):
    ics = []
    for path in clean_paths:
        ic, _ = load_clean_sample(path)
        ics.append(ic)
    training_params = torch.stack(ics, dim=0)
    torch.save(training_params, folder / 'training_params.pth')


def ensure_empty_output_dir(path, overwrite):
    if path.exists():
        if not overwrite:
            raise FileExistsError(
                f'Output directory already exists: {path}. '
                'Pass --overwrite to replace it.'
            )
        shutil.rmtree(path)
    path.mkdir(parents=True, exist_ok=True)


def next_output_path(folder, index):
    return folder / f'{index:06d}.pth'


def integrate_to_final_state(clean_path, args):
    ic, _ = load_clean_sample(clean_path)
    _, final_state = generate_ks_segment(
        initial_condition=ic.numpy().astype(np.float32),
        L=args.L,
        N=args.N,
        dt=args.dt,
        T=args.T_segment,
        t_res=args.t_res,
    )
    return final_state.astype(np.float32)


def format_noise_scale(scale):
    return float(scale)


def main():
    args = parse_args()

    source_root = Path(args.source_root).resolve()
    out_root = resolve_root(args.out_root, source_root)

    src_train = source_root / args.train_folder
    src_val = source_root / args.val_folder
    src_test = source_root / args.test_folder

    out_train = out_root / args.out_train_folder
    out_val = out_root / args.out_val_folder
    out_test = out_root / args.out_test_folder

    for src in [src_train, src_val, src_test]:
        if not src.exists():
            raise FileNotFoundError(f'Missing source folder: {src}')

    src_paths = {p.resolve() for p in [src_train, src_val, src_test]}
    dst_paths = {p.resolve() for p in [out_train, out_val, out_test]}
    if src_paths & dst_paths:
        raise ValueError(
            'Refusing to write into the current dataset folders. '
            'Use a different --out_root or output folder names.'
        )

    src_train_clean = clean_files(src_train)
    src_val_clean = clean_files(src_val)
    src_test_clean = clean_files(src_test)
    source_posttrain = src_val_clean + src_test_clean

    if args.target_n_val < len(src_val_clean):
        raise ValueError(
            f'target_n_val={args.target_n_val} is smaller than the existing '
            f'validation split ({len(src_val_clean)}).'
        )
    if args.target_n_val + args.target_n_test < len(source_posttrain):
        raise ValueError(
            'The requested val+test size is smaller than the number of existing '
            'post-train segments. This script only supports safe extension.'
        )

    ensure_empty_output_dir(out_train, args.overwrite)
    ensure_empty_output_dir(out_val, args.overwrite)
    ensure_empty_output_dir(out_test, args.overwrite)

    # Copy train exactly so the next experiments keep the same training trajectory.
    for pth_path in sorted(src_train.glob('*.pth')):
        shutil.copy2(pth_path, out_train / pth_path.name)

    new_val_clean = []
    new_test_clean = []
    reused_records = []

    posttrain_iter = iter(source_posttrain)

    for dst_index in range(args.target_n_val):
        try:
            src_clean = next(posttrain_iter)
        except StopIteration:
            break
        dst_clean = next_output_path(out_val, dst_index)
        copy_sample_bundle(src_clean, dst_clean)
        new_val_clean.append(dst_clean)
        reused_records.append({'source': str(src_clean), 'target': str(dst_clean)})

    for dst_index in range(args.target_n_test):
        try:
            src_clean = next(posttrain_iter)
        except StopIteration:
            break
        dst_clean = next_output_path(out_test, dst_index)
        copy_sample_bundle(src_clean, dst_clean)
        new_test_clean.append(dst_clean)
        reused_records.append({'source': str(src_clean), 'target': str(dst_clean)})

    need_generated = (
        args.target_n_val - len(new_val_clean)
        + args.target_n_test - len(new_test_clean)
    )

    current_state = None
    if need_generated > 0:
        if source_posttrain:
            continue_from = source_posttrain[-1]
        else:
            if not src_train_clean:
                raise ValueError('No clean train files found to continue the orbit from.')
            continue_from = src_train_clean[-1]
        current_state = integrate_to_final_state(continue_from, args)

    generated_records = []
    next_val_index = len(new_val_clean)
    next_test_index = len(new_test_clean)

    while next_val_index < args.target_n_val:
        segment_ic = current_state.copy()
        traj, current_state = generate_ks_segment(
            initial_condition=segment_ic,
            L=args.L,
            N=args.N,
            dt=args.dt,
            T=args.T_segment,
            t_res=args.t_res,
        )
        dst_clean = next_output_path(out_val, next_val_index)
        write_clean_sample(dst_clean, segment_ic, traj)
        new_val_clean.append(dst_clean)
        generated_records.append({'target': str(dst_clean), 'split': 'val'})
        next_val_index += 1

    while next_test_index < args.target_n_test:
        segment_ic = current_state.copy()
        traj, current_state = generate_ks_segment(
            initial_condition=segment_ic,
            L=args.L,
            N=args.N,
            dt=args.dt,
            T=args.T_segment,
            t_res=args.t_res,
        )
        dst_clean = next_output_path(out_test, next_test_index)
        write_clean_sample(dst_clean, segment_ic, traj)
        new_test_clean.append(dst_clean)
        generated_records.append({'target': str(dst_clean), 'split': 'test'})
        next_test_index += 1

    if len(new_val_clean) != args.target_n_val or len(new_test_clean) != args.target_n_test:
        raise RuntimeError('Failed to create the requested number of val/test files.')

    write_training_params(out_val, new_val_clean)
    write_training_params(out_test, new_test_clean)

    noise_scales = [format_noise_scale(scale) for scale in args.noise_scales]
    for noise_scale in noise_scales:
        save_noisy_ks_data(str(out_train), noise_scale, overwrite=False)
        save_noisy_ks_data(str(out_val), noise_scale, overwrite=False)
        save_noisy_ks_data(str(out_test), noise_scale, overwrite=False)

    metadata = {
        'source_root': str(source_root),
        'out_root': str(out_root),
        'source_split_sizes': {
            'train': len(src_train_clean),
            'val': len(src_val_clean),
            'test': len(src_test_clean),
        },
        'target_split_sizes': {
            'train': len(src_train_clean),
            'val': args.target_n_val,
            'test': args.target_n_test,
        },
        'solver': {
            'N': args.N,
            'L': args.L,
            'dt': args.dt,
            't_res': args.t_res,
            'T_segment': args.T_segment,
        },
        'noise_scales': noise_scales,
        'reused_records': reused_records,
        'generated_records': generated_records,
    }
    with open(out_root / 'extension_metadata.json', 'w', encoding='utf-8') as f:
        json.dump(metadata, f, indent=2)

    print('Created expanded KS single-trajectory split:')
    print(f'  train -> {out_train} ({len(src_train_clean)} clean segments, copied exactly)')
    print(f'  val   -> {out_val} ({len(new_val_clean)} clean segments)')
    print(f'  test  -> {out_test} ({len(new_test_clean)} clean segments)')
    print(f'  metadata -> {out_root / "extension_metadata.json"}')


if __name__ == '__main__':
    main()
